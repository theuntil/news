export function toSlug(input: string) {
  return input
    .trim()
    .toLowerCase()
    .replaceAll("ı", "i")
    .replaceAll("ğ", "g")
    .replaceAll("ü", "u")
    .replaceAll("ş", "s")
    .replaceAll("ö", "o")
    .replaceAll("ç", "c")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

export function fromSlugOrNull(slug: string) {
  const clean = toSlug(slug);
  return CITIES.find((c) => c.slug === clean) ?? null;
}
